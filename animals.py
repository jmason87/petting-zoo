from datetime import date


class Turtle:
    """class for Turtle"""
    def __init__(self, name, species):
        self.name = name
        self.species = species
        self.date_added = date.today()
        self.swimming = True

tom = Turtle("Tom", "Snapping Turtle")
print(tom.swimming)


